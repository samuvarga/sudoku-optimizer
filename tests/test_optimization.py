import unittest
from src.sudoku_solver import SudokuSolver
from src.algorithms.optimization import OptimizationAlgorithm

class TestOptimizationAlgorithm(unittest.TestCase):

    def setUp(self):
        self.solver = SudokuSolver()
        self.optimizer = OptimizationAlgorithm()

    def test_optimize(self):
        grid = [
            [5, 3, 0, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        optimized_grid = self.optimizer.optimize(grid)
        self.assertTrue(self.solver.solve(optimized_grid))

    def test_invalid_grid(self):
        grid = [
            [5, 3, 5, 0, 7, 0, 0, 0, 0],
            [6, 0, 0, 1, 9, 5, 0, 0, 0],
            [0, 9, 8, 0, 0, 0, 0, 6, 0],
            [8, 0, 0, 0, 6, 0, 0, 0, 3],
            [4, 0, 0, 8, 0, 3, 0, 0, 1],
            [7, 0, 0, 0, 2, 0, 0, 0, 6],
            [0, 6, 0, 0, 0, 0, 2, 8, 0],
            [0, 0, 0, 4, 1, 9, 0, 0, 5],
            [0, 0, 0, 0, 8, 0, 0, 7, 9]
        ]
        with self.assertRaises(ValueError):
            self.optimizer.optimize(grid)

if __name__ == '__main__':
    unittest.main()